def section(t):
    console.print()
    console.print(Rule(f"[bold {C_BLOOD}] {t} [/bold {C_BLOOD}]", style=C_RUST))
    console.print()

# ── Subprocess state ───────────────────────────────────────────────────────
_loki_proc: subprocess.Popen | None = None
_proc_lock  = threading.Lock()

# ── Helpers ────────────────────────────────────────────────────────────────
def _get_local_ip() -> str:
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.settimeout(2)
        s.connect(("8.8.8.8", 80))
        ip = s.getsockname()[0]
        s.close()
        return ip
    except Exception:
        return "127.0.0.1"

def _loki_installed() -> bool:
    return LOKI_LAUNCHER.exists()

def _loki_running() -> bool:
    global _loki_proc
    with _proc_lock:
        if _loki_proc is not None and _loki_proc.poll() is None:
            return True
        if LOKI_PID_FILE.exists():
            try:
                pid = int(LOKI_PID_FILE.read_text().strip())
                os.kill(pid, 0)
                return True
            except (ProcessLookupError, ValueError, PermissionError):
                LOKI_PID_FILE.unlink(missing_ok=True)
        return False

def _port_in_use(port: int) -> bool:
    try:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.settimeout(1)
            return s.connect_ex(("127.0.0.1", port)) == 0
    except Exception:
        return False

