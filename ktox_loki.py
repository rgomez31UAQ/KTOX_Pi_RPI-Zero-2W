def _ktox_wifi_check(self):
    """Check default route instead of Pager-specific wlan0cli interface."""
    try:
        r = subprocess.run(
            ['ip', 'route', 'show', 'default'],
            capture_output=True, text=True, timeout=5
        )
        self.wifi_connected = bool(r.stdout.strip())
    except Exception:
        self.wifi_connected = True
    return self.wifi_connected

_loki_mod.Loki.is_wifi_connected = _ktox_wifi_check

