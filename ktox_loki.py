# ── Install ────────────────────────────────────────────────────────────────
def install_loki() -> bool:
    section("Loki Installation")

    # Clone repo
    if not VENDOR_DIR.exists() or not (VENDOR_DIR / ".git").exists():
        info(f"Cloning {LOKI_REPO_URL} ...")
        VENDOR_DIR.parent.mkdir(parents=True, exist_ok=True)
        if VENDOR_DIR.exists():
            shutil.rmtree(VENDOR_DIR)
        r = subprocess.run(
            ["git", "clone", "--depth=1", LOKI_REPO_URL, str(VENDOR_DIR)],
            capture_output=True, text=True
        )
        if r.returncode != 0:
            err(f"Clone failed:\n{r.stderr.strip()}")
            return False
        ok("Repository cloned.")
    else:
        ok("Repository already present.")

    # Ensure nmap is available (Loki's vuln scanner needs it)
    if not shutil.which("nmap"):
        info("Installing nmap...")
        subprocess.run(["apt-get", "install", "-y", "nmap"], capture_output=True)

    # Write headless launcher into vendored directory
    _write_headless_launcher()
    ok("Headless launcher written.")

    # Create persistent data directories
    for sub in ["logs", "output/crackedpwd", "output/datastolen",
                "output/zombies", "output/vulnerabilities", "input"]:
        (LOKI_DATA_DIR / sub).mkdir(parents=True, exist_ok=True)
    ok(f"Data directory ready: {LOKI_DATA_DIR}")

    console.print()
    ok("[bold]Loki installed successfully.[/bold]")
    local_ip = _get_local_ip()
    info(f"Start with [ST], then open  http://{local_ip}:{LOKI_PORT}  in your browser.")
    return True


def update_loki() -> bool:
    section("Updating Loki")
    if not VENDOR_DIR.exists():
        err("Loki not installed. Run [IN] Install first.")
        return False
    r = subprocess.run(
        ["git", "-C", str(VENDOR_DIR), "pull"],
        capture_output=True, text=True
    )
    if r.returncode != 0:
        err(f"Update failed:\n{r.stderr.strip()}")
        return False
    _write_headless_launcher()
    ok("Loki updated and headless launcher refreshed.")
    return True


# ── Process management ─────────────────────────────────────────────────────
def start_loki() -> bool:
    global _loki_proc

    if _loki_running():
        local_ip = _get_local_ip()
        warn("Loki is already running.")
        info(f"Web UI: http://{local_ip}:{LOKI_PORT}")
        return False

    if not _loki_installed():
        err("Loki not installed. Choose [IN] to install first.")
        return False

    if _port_in_use(LOKI_PORT):
        err(f"Port {LOKI_PORT} is already in use by another process.")
        return False

    local_ip = _get_local_ip()
    env = os.environ.copy()
    env["LOKI_DATA_DIR"] = str(LOKI_DATA_DIR)
    env["BJORN_IP"]      = local_ip
    env["LOKI_PID_FILE"] = str(LOKI_PID_FILE)

    log_path = LOKI_DATA_DIR / "logs" / "ktox_loki_stdout.log"
    log_path.parent.mkdir(parents=True, exist_ok=True)
    log_fh = open(log_path, "a")

    with _proc_lock:
        _loki_proc = subprocess.Popen(
            [sys.executable, str(LOKI_LAUNCHER)],
            env=env,
            stdout=log_fh,
            stderr=subprocess.STDOUT,
            cwd=str(LOKI_DIR),
        )

    # Brief wait to confirm process is alive
    time.sleep(3)
    if not _loki_running():
        err("Loki process exited immediately — check logs with [LG].")
        return False

    ok(f"Loki started  (PID {_loki_proc.pid})")
    ok(f"Web UI →  http://{local_ip}:{LOKI_PORT}")
    ok(f"Stdout log: {log_path}")
    return True


def stop_loki() -> bool:
    global _loki_proc

    if not _loki_running():
        warn("Loki is not running.")
        return False

    with _proc_lock:
        if _loki_proc is not None:
            try:
                _loki_proc.terminate()
                _loki_proc.wait(timeout=10)
            except subprocess.TimeoutExpired:
                _loki_proc.kill()
            _loki_proc = None

        if LOKI_PID_FILE.exists():
            try:
                pid = int(LOKI_PID_FILE.read_text().strip())
                os.kill(pid, signal.SIGTERM)
                time.sleep(2)
                try:
                    os.kill(pid, signal.SIGKILL)
                except ProcessLookupError:
                    pass
            except (ValueError, ProcessLookupError):
                pass
            LOKI_PID_FILE.unlink(missing_ok=True)

    # Kill any orphaned nmap processes Loki may have left
    subprocess.run(["killall", "-q", "nmap"], capture_output=True)

    ok("Loki stopped.")
    return True


# ── Status panel ───────────────────────────────────────────────────────────
def _draw_status():
    running   = _loki_running()
    installed = _loki_installed()
    local_ip  = _get_local_ip()

    t = Table(box=box.SIMPLE, show_header=False, padding=(0, 2))
    t.add_column(style=C_STEEL, no_wrap=True)
    t.add_column(style=C_ASH)

    inst_str   = f"[{C_GOOD}]Yes[/{C_GOOD}]"   if installed else f"[{C_BLOOD}]No — run [IN] Install[/{C_BLOOD}]"
    status_str = f"[{C_GOOD}]● RUNNING[/{C_GOOD}]" if running  else f"[{C_DIM}]○ STOPPED[/{C_DIM}]"
    web_str    = (f"[{C_EMBER}]http://{local_ip}:{LOKI_PORT}[/{C_EMBER}]"
                  if running
                  else f"[{C_DIM}]http://{local_ip}:{LOKI_PORT} (offline)[/{C_DIM}]")

    t.add_row("Installed", inst_str)
    t.add_row("Status",    status_str)
    t.add_row("Web UI",    web_str)
    t.add_row("Data dir",  str(LOKI_DATA_DIR))
    t.add_row("Source",    str(LOKI_DIR) if installed else "not cloned")

    if running and _loki_proc is not None:
        t.add_row("PID", str(_loki_proc.pid))

    console.print(Panel(
        t,
        title=f"[bold {C_BLOOD}]  Loki Autonomous Engine  [/bold {C_BLOOD}]",
        border_style=C_RUST, box=box.HEAVY
    ))


def _tail_logs(n: int = 40):
    log_path = LOKI_DATA_DIR / "logs" / "ktox_loki_stdout.log"
    if not log_path.exists():
        warn("No log file yet — start Loki first.")
        return
    lines = log_path.read_text(errors="replace").splitlines()
    console.print()
    console.print(Rule(f"[{C_STEEL}]Last {n} lines — {log_path.name}[/{C_STEEL}]", style=C_DIM))
    for line in lines[-n:]:
        console.print(f"  [{C_DIM}]{line}[/{C_DIM}]")
    console.print()


# ── Menu ───────────────────────────────────────────────────────────────────
_LOKI_MENU = [
    ("ST", "Start Loki"),
    ("SP", "Stop  Loki"),
    ("SS", "Status"),
    ("LG", "View Logs (last 40 lines)"),
    ("IN", "Install / Re-install"),
    ("UP", "Update (git pull)"),
    ("BK", "Back to KTOx menu"),
]

def loki_menu():
    while True:
        console.print()
        console.print(Rule(f"[bold {C_BLOOD}] LOKI AUTONOMOUS ENGINE [/bold {C_BLOOD}]", style=C_RUST))
        _draw_status()
        console.print()
        for key, label in _LOKI_MENU:
            console.print(f"  [{C_BLOOD}][{key}][/{C_BLOOD}]  [{C_ASH}]{label}[/{C_ASH}]")
        console.print()

        try:
            choice = Prompt.ask(f"  [{C_BLOOD}]loki[/{C_BLOOD}]").strip().upper()
        except (KeyboardInterrupt, EOFError):
            return

        if choice == "ST":
            if not _loki_installed():
                if Confirm.ask(f"  [{C_ORANGE}]Loki not installed. Install now?[/{C_ORANGE}]"):
                    if not install_loki():
                        continue
                else:
                    continue
            start_loki()

        elif choice == "SP":
            stop_loki()

        elif choice == "SS":
            _draw_status()

        elif choice == "LG":
            _tail_logs()

        elif choice == "IN":
            if _loki_running():
                warn("Stop Loki before re-installing.")
            else:
                install_loki()

        elif choice == "UP":
            if _loki_running():
                warn("Stop Loki before updating.")
            else:
                update_loki()

        elif choice == "BK":
            return

        else:
            err("Invalid option.")
