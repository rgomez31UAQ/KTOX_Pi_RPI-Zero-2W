console = Console(highlight=False)

def tag(t, c=C_BLOOD): return f"[{c}]{t}[/{c}]"
def ok(t):   console.print(f"  [{C_GOOD}]✔  {t}[/{C_GOOD}]")
def warn(t): console.print(f"  [{C_ORANGE}]⚠  {t}[/{C_ORANGE}]")
